// ExportUsersToCSV exports all users to CSV format
// UC25: Export Users to CSV
func (uc *UsersUseCase) ExportUsersToCSV(ctx context.Context) ([]byte, error) {
	// Get all users
	users, err := uc.repo.ListAllUsersForExport(ctx)
	if err != nil {
		fmt.Printf("ListAllUsersForExport Error: %v\n", err)
		return nil, fmt.Errorf("failed to get users for export: %w", err)
	}

	// Create CSV buffer
	var buf bytes.Buffer
	writer := csv.NewWriter(&buf)

	// Write header
	header := []string{"Name", "Email", "Role", "Status", "Projects", "Last Active"}
	if err := writer.Write(header); err != nil {
		return nil, fmt.Errorf("failed to write CSV header: %w", err)
	}

	// Write data rows
	for _, u := range users {
		status := "Active"
		if !u.IsActive {
			status = "Inactive"
		}

		lastActive := "Never"
		if u.LastLogin != nil {
			lastActive = u.LastLogin.Format("2006-01-02 15:04:05")
		}

		row := []string{
			u.FullName,
			u.Email,
			strings.Title(u.Role),
			status,
			fmt.Sprintf("%d", u.ProjectCount),
			lastActive,
		}

		if err := writer.Write(row); err != nil {
			return nil, fmt.Errorf("failed to write CSV row: %w", err)
		}
	}

	writer.Flush()
	if err := writer.Error(); err != nil {
		return nil, fmt.Errorf("CSV writer error: %w", err)
	}

	return buf.Bytes(), nil
}

// Helper functions

// generateUsername generates a username from email
func generateUsername(email string) string {
	parts := strings.Split(email, "@")
	if len(parts) > 0 {
		return parts[0]
	}
	return email
}

// generateSecurePassword generates a secure random password
func generateSecurePassword(length int) string {
	const charset = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789!@#$%^&*"
	password := make([]byte, length)

	for i := range password {
		num, _ := rand.Int(rand.Reader, big.NewInt(int64(len(charset))))
		password[i] = charset[num.Int64()]
	}

	return string(password)
}

// hashPassword hashes a password using Argon2id
func hashPassword(password string) string {
	// Argon2id parameters
	salt := make([]byte, 16)
	rand.Read(salt)

	hash := argon2.IDKey([]byte(password), salt, 1, 64*1024, 4, 32)

	// Return base64 encoded hash (in production, you'd want to store salt separately)
	return fmt.Sprintf("%x", hash)
}
